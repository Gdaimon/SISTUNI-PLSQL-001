CREATE OR REPLACE PROCEDURE UPDATESALEMP
(CODIGO EMP.EMPNO%TYPE, SALARIO EMP.SAL%TYPE)
IS
    CONT NUMBER;
BEGIN
    SELECT COUNT(*) INTO CONT
        FROM EMP WHERE EMPNO = CODIGO;
    IF (CONT = 0) THEN 
        RAISE NO_DATA_FOUND;
    END IF;
    UPDATE EMP
        SET SAL = SALARIO
        WHERE EMPNO = CODIGO;
    COMMIT;
    DBMS_OUTPUT.PUT(' PROCESO OK ');
    EXCEPTION
        WHEN NO_DATA_FOUND THEN
        DBMS_OUTPUT.PUT(' CODIGO DE MUESTRA ');
END;


EXEC UPDATESALEMP (9999,5000);
